# ================== –§–£–ù–ö–¶–ò–Ø –î–õ–Ø GPT ==================
async def get_gpt_response(user_text: str) -> str:
    try:
        messages = [
            {"role": "system", "content": SYSTEM_PROMPT},
            {
                "role": "user",
                "content": (
                    "–í–æ—Ç –≤–Ω—É—Ç—Ä–µ–Ω–Ω—è—è –±–∞–∑–∞ –∑–Ω–∞–Ω–∏–π –ø–æ —Å—Ç—É–¥–∏–∏ BEARLAKE DETAILING & SHOP:\n"
                    f"{KNOWLEDGE_BASE}\n\n"
                    f"–í–æ–ø—Ä–æ—Å –∫–ª–∏–µ–Ω—Ç–∞: {user_text}"
                ),
            },
        ]

        completion = await client.chat.completions.create(
            model=OPENAI_MODEL,
            messages=messages,
            temperature=0.2,
            max_tokens=500,
        )

        # –õ–æ–≥–∏—Ä—É–µ–º —Ç–æ–∫–µ–Ω—ã
        prompt_tokens = completion.usage.prompt_tokens
        completion_tokens = completion.usage.completion_tokens
        total_tokens = completion.usage.total_tokens
        log_token_usage(prompt_tokens, completion_tokens, total_tokens)

        return completion.choices[0].message.content.strip()

    except Exception as e:
        logger.exception(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–ø—Ä–æ—Å–µ –∫ OpenAI: {e}")
        return (
            "–ò–∑–≤–∏–Ω–∏—Ç–µ, —Å–µ–π—á–∞—Å –Ω–µ —É–¥–∞–ª–æ—Å—å –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –∑–∞–ø—Ä–æ—Å. "
            "–ü–æ–ø—Ä–æ–±—É–π—Ç–µ, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, –µ—â—ë —Ä–∞–∑ —á—É—Ç—å –ø–æ–∑–∂–µ."
        )

# ================== –û–ë–†–ê–ë–û–¢–ß–ò–ö–ò –ö–û–ú–ê–ù–î ==================
async def start(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if update.message is None:
        return

    # –ü–æ–ª—É—á–∞–µ–º –∏—Å—Ç–æ—á–Ω–∏–∫ –≤—Ö–æ–¥–∞: /start website ‚Üí ["website"]
    args = context.args
    source = args[0] if args else "unknown"

    user_id = update.effective_user.id if update.effective_user else "unknown"
    logger.info(f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {user_id} –Ω–∞—á–∞–ª —á–∞—Ç –∏–∑ –∏—Å—Ç–æ—á–Ω–∏–∫–∞: {source}")

    # –õ–æ–≥–∏—Ä—É–µ–º –Ω–æ–≤–æ–≥–æ –∫–ª–∏–µ–Ω—Ç–∞
    log_new_client(user_id, source)

    greeting = (
        "–ó–¥—Ä–∞–≤—Å—Ç–≤—É–π—Ç–µ! –†–∞–¥ –∑–Ω–∞–∫–æ–º—Å—Ç–≤—É üëã –ü–æ–¥—Å–∫–∞–∂–∏—Ç–µ, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ –ø—Ä–∏–≤–µ—Å—Ç–∏ –≤ –ø–æ—Ä—è–¥–æ–∫ ‚Äî "
        "–∫—É–∑–æ–≤, —Å–∞–ª–æ–Ω –∏–ª–∏ –∫–æ–º–ø–ª–µ–∫—Å? –ò–Ω—Ç–µ—Ä–µ—Å–µ–Ω –ª–∏ –≤–∞–º –≤–∞—Ä–∏–∞–Ω—Ç –ø–æ—Å—Ç–∞ —Å–∞–º–æ–æ–±—Å–ª—É–∂–∏–≤–∞–Ω–∏—è "
        "–∏–ª–∏ —É–¥–æ–±–Ω–µ–µ –¥–æ–≤–µ—Ä–∏—Ç—å —Ä–∞–±–æ—Ç—É –º–∞—Å—Ç–µ—Ä–∞–º?"
    )
    await update.message.reply_text(greeting)


async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if update.message is None or update.message.text is None:
        return

    user_text = update.message.text.strip()
    chat_id = update.effective_chat.id
    user_id = update.effective_user.id if update.effective_user else "unknown"

    logger.info(f"–°–æ–æ–±—â–µ–Ω–∏–µ –æ—Ç {user_id}: {user_text!r}")

    # –õ–æ–≥–∏—Ä—É–µ–º –≤–æ–ø—Ä–æ—Å
    log_question(user_id, user_text)

    try:
        await context.bot.send_chat_action(chat_id=chat_id, action="typing")
    except Exception as e:
        logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –æ—Ç–ø—Ä–∞–≤–∏—Ç—å 'typing': {e}")

    text_lower = user_text.lower()

    # ---- –ñ–Å–°–¢–ö–ò–ô –ü–ï–†–ï–•–í–ê–¢ –ü–†–ï–ò–ú–£–©–ï–°–¢–í (–∫–∞–∫ –≤ —Å—Ç–∞—Ä–æ–º –±–æ—Ç–µ) ----
    if (
        "–ø—Ä–µ–∏–º—É—â–µ—Å—Ç–≤" in text_lower
        or "–ø–æ—á–µ–º—É –∏–º–µ–Ω–Ω–æ –≤—ã" in text_lower
        or "–ø–æ—á–µ–º—É –≤—ã–±—Ä–∞—Ç—å –≤–∞—Å" in text_lower
        or "—á–µ–º –≤—ã –ª—É—á—à–µ –¥—Ä—É–≥–∏—Ö" in text_lower
        or "—á–µ–º –≤—ã –æ—Ç–ª–∏—á–∞–µ—Ç–µ—Å—å" in text_lower
    ):
        await update.message.reply_text(ADVANTAGES_ANSWER, disable_web_page_preview=False)
        return
    # ----------------------------------------------------

    # ---- –û–°–¢–ê–õ–¨–ù–û–ï ‚Äî –ß–ï–†–ï–ó GPT (–∫–∞–∫ –≤ —Å—Ç–∞—Ä–æ–º –±–æ—Ç–µ) ----
    answer = await get_gpt_response(user_text)

    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ç–≤–µ—Ç
    await update.message.reply_text(answer, disable_web_page_preview=False)

# ================== –ó–ê–ü–£–°–ö ==================
def main() -> None:
    request = HTTPXRequest()
    application = (
        Application.builder()
        .token(TELEGRAM_BOT_TOKEN)
        .request(request)
        .build()
    )

    application.add_handler(CommandHandler("start", start))
    application.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    logger.info("–ë–æ—Ç –∑–∞–ø—É—â–µ–Ω. –û–∂–∏–¥–∞—é —Å–æ–æ–±—â–µ–Ω–∏—è...")
    application.run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()